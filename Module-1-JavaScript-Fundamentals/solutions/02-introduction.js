// Solution to Challenge 2: Personal Introduction

// Step 1: Create a variable with your name
let name = "Sam";

// Step 2: Create a variable with your favorite hobby
let hobby = "coding";

// Step 3: Print a message that includes both variables
console.log("My name is " + name + " and I enjoy " + hobby + ".");

// Alternative solution using template literals (a more modern approach)
// Template literals use backticks (`) and ${} to embed variables in strings
console.log(`My name is ${name} and I enjoy ${hobby}.`);

// This produces the same output but is often easier to read,
